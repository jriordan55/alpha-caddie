#!/usr/bin/env node
/**
 * Writes embedded-player-round-history.js (loaded before app.js) so round history is
 * available as window.__ALPHA_CADDIE_EMBEDDED_ROUND_HISTORY__ without fetching JSON
 * (works with file:// when this file is present).
 *
 * Skipped when ALPHA_CADDIE_EMBED_HISTORY=0.
 *
 * Run after: npm run build:history
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = path.resolve(__dirname, "..");
const OUT_JS = path.join(WEB_ROOT, "embedded-player-round-history.js");
const HISTORY_JSON = path.join(WEB_ROOT, "player_round_history.json");

if (process.env.ALPHA_CADDIE_EMBED_HISTORY === "0") {
  console.log("[embed-player-history] ALPHA_CADDIE_EMBED_HISTORY=0 — skip.");
  process.exit(0);
}

let data = null;
if (fs.existsSync(HISTORY_JSON)) {
  try {
    data = JSON.parse(fs.readFileSync(HISTORY_JSON, "utf8"));
  } catch (e) {
    console.warn("[embed-player-history] Bad JSON, writing null assignment:", e.message || e);
    data = null;
  }
} else {
  console.warn("[embed-player-history] No player_round_history.json — writing null.");
}

const head =
  "/**\n * Auto-generated by scripts/embed-player-history.mjs — do not edit.\n */\n" +
  "window.__ALPHA_CADDIE_EMBEDDED_ROUND_HISTORY__ = ";
const tail = ";\n";

/** Single write; avoids splicing multi‑MB literals into app.js. */
fs.writeFileSync(OUT_JS, head + JSON.stringify(data, null, 2) + tail, "utf8");
console.log("[embed-player-history] Wrote", path.basename(OUT_JS));
